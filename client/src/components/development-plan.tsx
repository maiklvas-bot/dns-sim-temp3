function getLearningBenefit(compId: string, item: string, fallbackDescription: string) {
  const normalized = item.toLowerCase();

  if (normalized.includes("хороший стресс")) {
    return "Поможет использовать стресс как рабочий ресурс и эффективнее распределять время под нагрузкой.";
  }

  if (normalized.includes("деловой переписки")) {
    return "Улучшит качество формулировок в письмах и снизит риск конфликтов из-за неоднозначной коммуникации.";
  }

  if (normalized.includes("gtd") || normalized.includes("джедайские техники")) {
    return "Даст инструменты для приоритизации задач, разгрузки головы и более устойчивого планирования смены.";
  }

  if (normalized.includes("делегирование")) {
    return "Поможет передавать задачи без потери контроля и высвобождать время на управление магазином.";
  }

  if (normalized.includes("лидер")) {
    return "Усилит управленческую позицию и поможет гибче подбирать стиль руководства под ситуацию.";
  }

  if (compId === "planning") {
    return "Улучшит качество планирования смены и поможет распределять время, людей и приоритеты без перегруза.";
  }

  if (compId === "communication") {
    return "Сделает коммуникацию яснее, спокойнее и повысит качество обратной связи с командой и клиентами.";
  }

  if (compId === "delegation") {
    return "Поможет безопасно делегировать задачи и масштабировать свою эффективность через команду.";
  }

  if (compId === "stress_resistance") {
    return "Укрепит устойчивость под давлением и снизит вероятность эмоциональных или поспешных решений.";
  }

  if (compId === "result_orientation") {
    return "Поможет связывать ежедневные действия с KPI магазина и работать на измеримый результат.";
  }

  return `Поддержит развитие компетенции: ${fallbackDescription.toLowerCase()}`;
}

