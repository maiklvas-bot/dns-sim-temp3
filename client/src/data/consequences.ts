function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function formatDeltaValue(entry: MetricDeltaEntry) {
  const sign = entry.delta > 0 ? "+" : "";

  switch (entry.unit) {
    case "rub":
      return `${sign}${entry.delta.toLocaleString("ru-RU")}₽`;
    case "kRub":
      return `${sign}${entry.delta}K₽`;
    case "percent":
      return `${sign}${entry.delta}%`;
    case "minutes":
      return `${sign}${entry.delta} мин`;
    case "score":
      return `${sign}${entry.delta.toLocaleString("ru-RU", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
    case "count":
    default:
      return `${sign}${entry.delta}`;
  }
}

function getExplanationPool(entry: MetricDeltaEntry, improved: boolean) {
  switch (entry.key) {
    case "customersInStore":
      return improved ? positiveTemplates.customers : negativeTemplates.customers;
    case "avgCheck":
      return improved ? positiveTemplates.avgCheck : negativeTemplates.avgCheck;
    case "conversion":
      return improved ? positiveTemplates.conversion : negativeTemplates.conversion;
    case "nps":
      return improved ? positiveTemplates.nps : negativeTemplates.nps;
    case "pickupSpeed":
      return improved ? positiveTemplates.pickupSpeed : negativeTemplates.pickupSpeed;
    case "warehouseLoad":
      return improved ? positiveTemplates.warehouseLoad : negativeTemplates.warehouseLoad;
    case "teamMorale":
      return improved ? positiveTemplates.teamMorale : negativeTemplates.teamMorale;
    case "dailyRevenue":
      return improved ? positiveTemplates.revenue : negativeTemplates.revenue;
    default:
      return improved ? positiveTemplates.revenue : negativeTemplates.revenue;
  }
}

export function generateConsequences(changes: MetricDeltaEntry[]): ConsequenceExplanation[] {
  return changes
    .filter((entry) => entry.delta !== 0)
    .sort((left, right) => Math.abs(right.delta) - Math.abs(left.delta))
    .slice(0, 5)
    .map((entry) => {
      const improved = entry.betterWhen === "lower" ? entry.delta < 0 : entry.delta > 0;
      return {
        metric: entry.metric,
        icon: improved ? "📈" : "📉",
        direction: improved ? "up" : "down",
        value: entry.delta,
        displayValue: formatDeltaValue(entry),
        explanation: pickRandom(getExplanationPool(entry, improved)),
      };
    });
}

