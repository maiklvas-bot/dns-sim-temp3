export function generateDevelopmentPlan(
  competencyId: string,
  competencyName: string,
  score: number,
): DevelopmentPlanItem {
  const now = new Date();
  const retestDate = new Date(now.getTime() + 90 * 24 * 60 * 60 * 1000);
  const template = devPlanTemplates[competencyId] || devPlanTemplates.planning;

  return {
    competencyId,
    competencyName,
    currentScore: score,
    weeks: [
      {
        period: "Неделя 1-4: Теория и базовые навыки",
        study: template.study[0],
        practice: template.practice[0],
        result: template.results[0],
      },
      {
        period: "Неделя 5-8: Продвинутая практика",
        study: template.study[1],
        practice: template.practice[1],
        result: template.results[1],
      },
      {
        period: "Неделя 9-12: Закрепление",
        study: template.study[2],
        practice: template.practice[2],
        result: template.results[2],
      },
    ],
    retestDate: retestDate.toLocaleDateString("ru-RU", { day: "numeric", month: "long", year: "numeric" }),
  };
}
